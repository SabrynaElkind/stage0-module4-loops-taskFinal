public class Power {
    public void printPower(int numberToPrint, int power) {
        for (int i = 1;i<=1;i++){

            double z = Math.pow(numberToPrint,power);
            int value = (int) z;

            System.out.println(value);



        }}

        public static void main (String[]args){
            new Power().printPower(10, 3);
        }
    }
